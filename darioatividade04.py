# Escreva um numero e veja qual o seu antecessor e seu sucessor! 
print("Descubra o sucessor e o antecessor de um número qualquer: ")
número = int (input('digite um número'))
sucessor = (número+1)
antecessor = (número-1)
print(f'o sucessor do seu número é {sucessor} e o antecessor é {antecessor}')